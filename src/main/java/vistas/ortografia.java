    private void btnVerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerificarActionPerformed

        String rUno = this.brazo.getText();

        String rDos = this.vena.getText();

        String rTres = this.blusa.getText();

        String rCuatro = this.abeja.getText();

        if (rUno.equals("B")) {

            JOptionPane.showMessageDialog(null, "La primer palabra es correcta!");

        } else {

            JOptionPane.showMessageDialog(null, "La primer palabra es incorrecta!");
              this.brazo.setText(null);

        }

        if (rDos.equals("V")) {

            JOptionPane.showMessageDialog(null, "La Segunda palabra es correcta!");

        } else {

            JOptionPane.showMessageDialog(null, "La Segunda palabra es incorrecta!");
              this.vena.setText(null);

        }
        
        
         if (rTres.equals("B")) {

            JOptionPane.showMessageDialog(null, "La Tercer palabra es correcta!");

        } else {

            JOptionPane.showMessageDialog(null, "La Tercer palabra es incorrecta!");
              this.blusa.setText(null);

        }
         
         if (rCuatro.equals("B")) {

            JOptionPane.showMessageDialog(null, "La Cuarta palabra es correcta!");

        } else {

            JOptionPane.showMessageDialog(null, "La Cuarta palabra es incorrecta!");
            
            
            this.abeja.setText(null);

        }



    }//GEN-LAST:event_btnVerificarActionPerformed

